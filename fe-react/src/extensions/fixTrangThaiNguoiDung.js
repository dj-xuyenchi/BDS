export const fixTrangThaiNguoiDung = (input) => {
    switch (input) {
        case 1:
            return "Tạm khóa";
        case 2:
            return "Đang làm việc";
        case 3:
            return "Đã khóa";
        case 4:
            return "Tạm khóa";
        default:
            return "Không rõ"

    }
}